def send_email(req: NotifyRequest) -> tuple[bool, str]:
    """
    Build and send the HTML summary alert email to the teacher.
    Returns (success: bool, error_message: str).
    """
    subject = f"Attendance Alert — {len(req.students)} student(s) require attention"

    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"]    = ALERT_SENDER
    msg["To"]      = req.teacher.email

    html_body = _build_teacher_html(req)
    msg.attach(MIMEText(html_body, "html"))

    try:
        with smtplib.SMTP(SMTP_SERVER, SMTP_PORT, timeout=15) as server:
            server.ehlo()
            server.starttls()
            server.ehlo()
            server.login(SMTP_USER, SMTP_PASSWORD)
            server.sendmail(ALERT_SENDER, req.teacher.email, msg.as_string())
        logger.info(f"Teacher email sent to {req.teacher.email} — {len(req.students)} students")
        return True, ""
    except smtplib.SMTPAuthenticationError:
        err = "SMTP authentication failed — check SMTP_USER and SMTP_PASSWORD (use Gmail App Password)"
        logger.error(err)
        return False, err
    except smtplib.SMTPException as e:
        err = f"SMTP error: {e}"
        logger.error(err)
        return False, err
    except Exception as e:
        err = f"Unexpected email error: {e}"
        logger.error(err)
        return False, err


