def send_student_email(student: StudentAlert, teacher_name: str) -> tuple[bool, str]:
    """
    Send a personalized attendance warning email directly to the student.
    Returns (success: bool, error_message: str).
    Silently skips (returns False, reason) if student has no email address.
    """
    if not student.student_email:
        reason = f"No email on record for student {student.usn} — skipped"
        logger.info(reason)
        return False, reason

    subject = f"Attendance Warning — {len(student.subjects)} subject(s) below threshold | {student.usn}"

    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"]    = ALERT_SENDER
    msg["To"]      = student.student_email

    html_body = _build_student_html(student, teacher_name)
    msg.attach(MIMEText(html_body, "html"))

    try:
        with smtplib.SMTP(SMTP_SERVER, SMTP_PORT, timeout=15) as server:
            server.ehlo()
            server.starttls()
            server.ehlo()
            server.login(SMTP_USER, SMTP_PASSWORD)
            server.sendmail(ALERT_SENDER, student.student_email, msg.as_string())
        logger.info(f"Student email sent to {student.student_email} ({student.usn})")
        return True, ""
    except smtplib.SMTPAuthenticationError:
        err = "SMTP authentication failed — check SMTP_USER and SMTP_PASSWORD"
        logger.error(err)
        return False, err
    except smtplib.SMTPException as e:
        err = f"SMTP error sending to {student.student_email}: {e}"
        logger.error(err)
        return False, err
    except Exception as e:
        err = f"Unexpected error sending to {student.student_email}: {e}"
        logger.error(err)
        return False, err
